package hari042;

public class Main {

    public static void main(String[] args) {
        int nilai1 = 23;
        int nilai2 = 24;

        System.out.println("Operator Perbandingan sama dengan");
        boolean samaDengan = nilai1 == nilai2;
        System.out.println(samaDengan);

        System.out.println("Operator Perbandingan tidak sama dengan");
        boolean tidakSamaDengan = nilai1 != nilai2;
        System.out.println(tidakSamaDengan);
    }
}
