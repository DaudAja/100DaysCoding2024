package hari016;

public class Main {
    public static void main(String[] args) {
        //Tipe data float adalah tipe data yang menyimpan bilangan desimal yang rangenya lebih kecil dari double
        
        //Deklarasi variabel tipe data float
        float desimalKecil;
        
        //Inisialisasi variabel tipe data float
        desimalKecil = 45.7f;
        
        //Mencetak variabel
        System.out.println("Tipe data float = "+desimalKecil);
        
    }
}
