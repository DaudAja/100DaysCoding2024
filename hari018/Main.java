package hari018;

public class Main {

    public static void main(String[] args) {
        //tipe data boolean adalah tipe data primitif yang hanya menyimpan dua nilai yaitu true dan false
        //deklarasi variabel
        boolean manusia, siswa;

        //inisialisai variabel
        manusia = true;
        siswa = false;

        //mencetak variabel
        System.out.printf("Apakah saya manusia : %b %nApakah saya   siswa : %b%n", manusia, siswa);
    }
}
